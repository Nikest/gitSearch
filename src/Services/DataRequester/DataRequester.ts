import { storeInterface, eventEmitter } from 'Services';

const requester = function (url): Promise<any> {
    return new Promise<JSON>((res, rej) => {
        const xhr = new XMLHttpRequest();

        xhr.open('GET', url, true);
        xhr.onloadend = function () {
            const data: JSON = JSON.parse(xhr.response);

            if (xhr.status === 200) {
                res(data);
                return;
            }

            rej(data);
        };

        xhr.onerror = function() {
            rej({message: 'Try again later'});
        };

        xhr.send();
    });
};

